package may.day14;

/**
 * FindPrimeNum类用于计算指定范围内质数的数量，并列出所有质数。
 * <p>
 * main方法说明：
 * - 无参数输入
 * - 无返回值
 * 方法主要执行过程：
 * 1. 初始化质数计数器为0。
 * 2. 遍历101到200之间的所有数字。
 * 3. 对于每个数字，通过检查其能否被2到自身之前的任何数字整除来判断是否为质数。
 * 4. 如果发现数字能被2到自身之前的某个数字整除，则标记为非质数，进入下一个数字的判断。
 * 5. 如果数字不能被2到自身之前的任何数字整除，则标记为质数，计数器加一，并打印该数字。
 * 6. 最终输出质数的总数。
 */
public class FindPrimeNum {
    public static void main(String[] args) {
        // 计算101 ~ 200之间质数的数量，并打印所有质数
        int count = 0; // 质数计数器
        for (int i = 101; i <= 200; i++) { // 遍历101到200之间的所有数字
            boolean flag = true; // 假设当前数字为质数
            for (int j = 2; j < i; j++) { // 检查当前数字是否为质数
                if (i % j == 0) { // 如果当前数字能被j整除，则不是质数
                    flag = false; // 标记为非质数
                    break; // 结束内层循环
                }
            }
            if (flag) { // 如果经过检查确定为质数
                count++; // 质数计数器加一
                System.out.println(i); // 打印质数
            }
        }
        System.out.println("101 ~ 200之间质数的数量为：" + count);
    }
}
